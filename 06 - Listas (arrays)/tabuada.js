function soma(pNumero) {
    const numero = pNumero;
    for (let i = 1; i <= 10; i++) {
        console.log(`${numero} + ${i} = ${(numero+i)}`)
    }
}
soma(6);

function divisao(pNumero) {
    const numero = pNumero;
    for (let i = 1; i <= 10; i++) {
        console.log(`${numero} / ${i} = ${(numero/i)}`)
    }
}

divisao(6);

function multiplicacao(pNumero) {
    const numero = pNumero;
    for (let i = 1; i <= 10; i++) {
        console.log(`${numero} * ${i} = ${(numero*i)}`)
    }
}

multiplicacao(8)

